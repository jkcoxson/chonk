package me.logwet.chonk.config.object;

public class Updates {
    private Boolean dust;
    private Boolean rails;
    private Boolean rsTiles;
    private Boolean pistons;
    private Boolean ore;
    private Boolean air;
    private Boolean fluid;

    public Updates() {
    }

    public Boolean getDust() {
        return dust;
    }

    public void setDust(Boolean dust) {
        this.dust = dust;
    }

    public Boolean getRails() {
        return rails;
    }

    public void setRails(Boolean rails) {
        this.rails = rails;
    }

    public Boolean getRsTiles() {
        return rsTiles;
    }

    public void setRsTiles(Boolean rsTiles) {
        this.rsTiles = rsTiles;
    }

    public Boolean getPistons() {
        return pistons;
    }

    public void setPistons(Boolean pistons) {
        this.pistons = pistons;
    }

    public Boolean getOre() {
        return ore;
    }

    public void setOre(Boolean ore) {
        this.ore = ore;
    }

    public Boolean getAir() {
        return air;
    }

    public void setAir(Boolean air) {
        this.air = air;
    }

    public Boolean getFluid() {
        return fluid;
    }

    public void setFluid(Boolean fluid) {
        this.fluid = fluid;
    }
}